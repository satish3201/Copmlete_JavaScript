// Parameters and Agruments

let a = 10;
let b = 20;


// Parameters: These are the named variables listed in a function's definition. They act as placeholders for the values that will be passed into the function when it is called. Parameters are defined within the parentheses following the function name.

function add(fNum, sNum){
  const sum = fNum + sNum;
  console.log(sum);
}


// Arguments: These are the actual values or expressions passed to a function when it is invoked or called. These values are assigned to the corresponding parameters in the order they are provided.

add(100, 200);


